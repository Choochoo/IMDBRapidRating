import { access } from "node:fs/promises";
import path from "node:path";

const GeneratedNotice = "<!-- Generated by npm run docs:setup. Edit src/app/setup-guide-definitions.js instead. -->";
const PendingVerificationLabel = "Pending screenshot capture";
const CapturedStatus = "captured";
const Newline = "\n";
const ScreenshotSafetyDefinitions = [
  "Use disposable test accounts and non-personal sample data.",
  "Cover cookies, API keys, emails, names, and identifiers with solid opaque blocks before saving.",
  "Never blur a secret and never place an unredacted source image in the repository.",
  "Inspect the final image at full resolution and remove metadata before adding it."
];
const ScreenshotSafety = Object.freeze(ScreenshotSafetyDefinitions);
const ScreenshotSafetyNotice = `> Security: ${ScreenshotSafety.join(" ")}`;
const SetupIndexHeaderDefinitions = [
  GeneratedNotice,
  "",
  "# Illustrated setup guides",
  "",
  "Short, visual walkthroughs for the connections and imports that make Rapid Rater more useful.",
  "",
  ScreenshotSafetyNotice,
  "",
  "| Guide | Why use it? | Screenshots |",
  "| --- | --- | --- |"
];
const SetupIndexFooterDefinitions = [
  "",
  "Administrator: [Migrate production secrets to Azure Key Vault](azure-key-vault-migration.md).",
  "",
  "Run `npm run docs:setup` after changing tutorial copy or adding screenshots.",
  "Run `npm run --silent setup:screenshots` for the machine-readable capture checklist."
];
const SetupIndexHeader = Object.freeze(SetupIndexHeaderDefinitions);
const SetupIndexFooter = Object.freeze(SetupIndexFooterDefinitions);

export async function BuildScreenshotChecklist(flows, rootPath) {
  const screenshots = await Promise.all(flows.flatMap((flow) => flow.steps.map((step) => BuildScreenshotRecord(flow, step, rootPath))));
  const captured = screenshots.filter((item) => item.status === CapturedStatus).length;
  return {
    version: 1,
    command: "npm run --silent setup:screenshots",
    safety: ScreenshotSafety,
    summary: { total: screenshots.length, captured, needed: screenshots.length - captured },
    screenshots
  };
}

export function RenderSetupGuideIndex(flows, checklist) {
  const rows = flows.map((flow) => RenderIndexRow(flow, checklist)).join(Newline);
  const lines = [...SetupIndexHeader, rows, ...SetupIndexFooter];
  return `${lines.join(Newline)}${Newline}`;
}

export function RenderSetupGuide(flow, checklist) {
  const records = checklist.screenshots.filter((item) => item.flowId === flow.id);
  const sections = flow.steps.map((step, index) => RenderSetupStep(step, index, records[index])).join("\n\n");
  const lines = [GeneratedNotice, "", `# ${flow.title}`, "", flow.summary, "", ScreenshotSafetyNotice, "", sections];
  return `${lines.join(Newline)}${Newline}`;
}

export function SetupGuideDocumentName(flow) {
  return `${flow.id}.md`;
}

function BuildScreenshotFields(flow, step, file) {
  return {
    flowId: flow.id,
    flowTitle: flow.title,
    stepId: step.id,
    stepTitle: step.title,
    file,
    imageSrc: step.imageSrc,
    alt: step.imageAlt,
    capture: step.capture,
    redact: step.redact,
    lastVerified: step.verifiedOn || null
  };
}

async function BuildScreenshotRecord(flow, step, rootPath) {
  const file = step.imageSrc.replace(/^\/+/, "");
  const fields = BuildScreenshotFields(flow, step, file);
  const absolutePath = path.join(rootPath, ...file.split("/"));
  const status = await PathExists(absolutePath) ? CapturedStatus : "needed";
  return { ...fields, status };
}

function RenderAction(action) {
  if (!action)
    return "";
  if (action.kind === "external")
    return `[${action.label}](${action.href})`;
  return `In Rapid Rater: **${action.label}**`;
}

function RenderIndexRow(flow, checklist) {
  const records = checklist.screenshots.filter((item) => item.flowId === flow.id);
  const captured = records.filter((item) => item.status === CapturedStatus).length;
  return `| [${flow.title}](${SetupGuideDocumentName(flow)}) | ${flow.summary} | ${captured}/${records.length} captured |`;
}

function RenderRedactions(record) {
  if (!record.redact.length)
    return "None beyond the universal safety rules.";
  return record.redact.join(", ");
}

function RenderScreenshot(record) {
  if (record.status === CapturedStatus)
    return `![${record.alt}](../../${record.file})`;
  return `> **Screenshot needed:** ${record.capture}\n>\n> **Solid-redact:** ${RenderRedactions(record)}`;
}

function RenderSetupStep(step, index, record) {
  const lines = [`## ${index + 1}. ${step.title}`, "", step.body];
  const action = RenderAction(step.action);
  if (action)
    lines.push("", action);
  lines.push("", RenderScreenshot(record));
  const verified = record.lastVerified || PendingVerificationLabel;
  lines.push("", `Last verified: ${verified}.`);
  return lines.join(Newline);
}

function PathExists(filePath) {
  return access(filePath).then(() => true, () => false);
}
